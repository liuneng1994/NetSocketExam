package com.hand.Exam3;

public class Stock {
    public final String name;
    public final String open;
    public final String close;
    public final String current;
    public final String hign;
    public final String low;

    public Stock(String name, String open, String close, String current, String hign, String low) {
        super();
        this.name = name;
        this.open = open;
        this.close = close;
        this.current = current;
        this.hign = hign;
        this.low = low;
    }
}
